# Condicional Composta

# Desenvolva um programa que lê um valor inteiro e descobre se ele é par ou impar.

# Exercício 1:

valor = int(input('Digite um valor: '))
if ( valor % 2 == 0 ): # % = o mod | resto da divisão. Neste resulatdo se o resto for zero o número é par
    print('O número %i é Par' %valor)
else:
    print('O número %i é Impar' %valor)

